class Radius
{
  public static void main(String[]args)
  {
   int radius=5;
   int diameter=7;
   int circumference=8;
   

   diameter=2*radius;
   circumference=2*31*radius;
   int area=2*31*(radius*radius);

   System.out.println("Total diameter is"+diameter);
   System.out.println("Total circumference is"+ circumference);
   System.out.println("Total area is"+ area);


   }




 }